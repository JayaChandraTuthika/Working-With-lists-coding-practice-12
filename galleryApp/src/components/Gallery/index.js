class Gallery extends Component {
  state = {
    selectedImageId: imagesList[0].id,
  }

  onClickChangeImage = id => {
    this.setState({
      selectedImageId: id,
    })
  }

  render() {
    const {selectedImageId} = this.state

    const mainImage = imagesList.find(each => each.id === selectedImageId)
    const {imageAltText, imageUrl} = mainImage

    return (
      <div className="bg-container">
        <img src={imageUrl} alt={imageAltText} className="main-image" />
        <h1 className="heading">Nature Photography</h1>
        <p className="description">Nature Photography by Rahul</p>
        <ul className="list-container">
          {imagesList.map(each => (
            <ThumbnailItem
              imageDetails={each}
              isActive={each.id === selectedImageId}
              onClickChangeImage={this.onClickChangeImage}
              key={each.id}
            />
          ))}
        </ul>
      </div>
    )
  }
}

export default Gallery
